CREATE table BENCHMARK (
    SEX	        VARCHAR2(20),
    UPPER_AGE 	VARCHAR2(20),
    LOWER_AGE 	VARCHAR2(20),
    A16KM 	VARCHAR2(20),
    BEAM	VARCHAR2(20),
    PUSH_UP	VARCHAR2(20),
    A32KM	VARCHAR2(20),
    HORIZONTAL_ROPE	VARCHAR2(20),
    A6FT_WALL 	VARCHAR2(20),
    SWIMMING	VARCHAR2(20),
    REACH_UP	VARCHAR2(20),
    SIT_UP	VARCHAR2(20)
);

CREATE TABLE OFFICER(
    BA_NO VARCHAR2(20),
    NAME VARCHAR2(20),
    D DATE,
    RANK VARCHAR2(20),

    constraint OFFICER_BA_NO_PK PRIMARY KEY(BA_NO) 
);

CREATE TABLE SOLDIER( 
    SOLDIER_NO VARCHAR2(10),
    name VARCHAR2(20), 
    JOIN_date DATE, 
    SVC_LENGTH  VARCHAR2(25), 
    AGE VARCHAR2(20), 
    email VARCHAR2(25) , 
    CELL_no VARCHAR2(11),  
    
    constraint SOLDIER_SOLDIER_NO_pk primary key(SOLDIER_NO)
) ;

CREATE TABLE IPFT_INFO(
	BIANNUAL  VARCHAR2(10),
    BIANNUAL_DATE DATE,

    constraint IPFT_INFO_BIANNUAL_DATE_PK PRIMARY KEY(BIANNUAL_DATE)
);

CREATE TABLE ITEM_INFO(
    ITEM_ID  VARCHAR2(10),
    ITEM_NAME VARCHAR2(20),

    constraint ITEM_INFO_ITEM_ID_PK PRIMARY KEY(ITEM_ID)
);

CREATE TABLE OIC(
    BIANNUAL_DATE DATE,
    BA_NO VARCHAR2(20),
    
    constraint OIC_BIANNUAL_DATE_fk foreign key(BIANNUAL_DATE) references IPFT_INFO(BIANNUAL_DATE) on delete cascade,
    constraint OIC_BA_NO_fk foreign key(BA_NO) references OFFICER(BA_NO) on delete cascade
);	

CREATE table TOTAL_RESULT(
    SOLDIER_NO VARCHAR2(10),
    BIANNUAL_DATE DATE,
    RESULT VARCHAR2(20),

    constraint TOTAL_RESULT_SOLDIER_NO_fk foreign key(SOLDIER_NO) references SOLDIER(SOLDIER_NO) on delete cascade,
    constraint TOTAL_RESULT_BIANNUAL_DATE_fk foreign key(BIANNUAL_DATE) references IPFT_INFO(BIANNUAL_DATE) on delete cascade
);

CREATE TABLE FAILED_P_I(
	SOLDIER_NO  VARCHAR2(10),
    ITEM_ID VARCHAR2(10), 
    BIANNUAL_DATE DATE,

    constraint FAILED_P_I_SOLDIER_NO_fk foreign key(SOLDIER_NO) references SOLDIER(SOLDIER_NO) on delete cascade,
    constraint FAILED_P_I_BIANNUAL_DATE_fk foreign key(BIANNUAL_DATE) references IPFT_INFO(BIANNUAL_DATE) on delete cascade,
    constraint FAILED_P_I_ITEM_ID_fk foreign key(ITEM_ID) references ITEM_INFO(ITEM_ID) on delete cascade
);


